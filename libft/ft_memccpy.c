#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t	index;
	UC		chr;

	chr = c;
	index = 0;
	while (index < n)
	{
		if ((((UC *)dst)[index] = ((UC *)src)[index]) == chr)
			return (dst + index + 1);
		index++;
	}
	return (NULL);
}
