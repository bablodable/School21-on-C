#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	size_t	nb;
	void	*addr;

	nb = count * size;
	addr = malloc(nb);
	if (addr)
		ft_memset(addr, 0, nb);
	return (addr);
}
