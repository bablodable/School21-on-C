#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	UC *a1;
	UC *a2;

	if (n == 0)
		return (0);
	a1 = (UC *)s1;
	a2 = (UC *)s2;
	while (*a1 == *a2 && n - 1 > 0)
	{
		a1++;
		a2++;
		n--;
	}
	return (*a1 - *a2);
}
