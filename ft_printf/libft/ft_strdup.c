#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*duple;

	len = ft_strlen(s1);
	if (!(duple = ft_calloc(len + 1, sizeof(char))))
		return (NULL);
	ft_memcpy(duple, s1, len);
	duple[len] = '\0';
	return (duple);
}
